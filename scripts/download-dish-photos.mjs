// Reads all .url Windows shortcuts in the project root, downloads each linked
// image, and writes them to /public/assets/photos/dishes/<slug>.<ext>.
// Also emits a typed manifest at src/lib/dish-photos.ts mapping slug → src.

import { readFileSync, readdirSync, mkdirSync, writeFileSync, existsSync } from "node:fs";
import { resolve, extname } from "node:path";
import { pipeline } from "node:stream/promises";
import { createWriteStream } from "node:fs";
import { Readable } from "node:stream";

const ROOT = resolve(process.cwd(), "..");
const OUT_DIR = resolve(process.cwd(), "public", "assets", "photos", "dishes");
const MANIFEST = resolve(process.cwd(), "src", "lib", "dish-photos.ts");

mkdirSync(OUT_DIR, { recursive: true });

function slugify(name) {
  return name
    .normalize("NFD")
    .replace(/[̀-ͯ]/g, "")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

const urlFiles = readdirSync(ROOT).filter((f) => f.endsWith(".url"));
console.log(`Found ${urlFiles.length} .url shortcuts`);

const manifest = {};
let downloaded = 0;
let skipped = 0;
let failed = 0;

for (const file of urlFiles) {
  const dishName = file.replace(/\.url$/, "");
  const slug = slugify(dishName);

  // Skip generic DSC photos — they are decorative and were already copied
  // separately in the main asset pass.
  if (/^dsc\d/i.test(dishName)) {
    skipped++;
    continue;
  }

  const content = readFileSync(resolve(ROOT, file), "utf8");
  const match = content.match(/URL=(.+)/);
  if (!match) {
    failed++;
    continue;
  }
  const url = match[1].trim();
  const ext = extname(new URL(url).pathname).toLowerCase() || ".jpg";
  const outName = `${slug}${ext}`;
  const outPath = resolve(OUT_DIR, outName);

  if (existsSync(outPath)) {
    manifest[slug] = `/assets/photos/dishes/${outName}`;
    skipped++;
    continue;
  }

  try {
    const res = await fetch(url, {
      headers: {
        "User-Agent":
          "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36",
      },
    });
    if (!res.ok || !res.body) {
      console.warn(`  ✗ ${dishName}: HTTP ${res.status}`);
      failed++;
      continue;
    }
    await pipeline(Readable.fromWeb(res.body), createWriteStream(outPath));
    manifest[slug] = `/assets/photos/dishes/${outName}`;
    downloaded++;
    console.log(`  ✓ ${dishName} → ${outName}`);
  } catch (e) {
    console.warn(`  ✗ ${dishName}: ${e.message}`);
    failed++;
  }
}

const sortedKeys = Object.keys(manifest).sort();
const lines = [
  "// AUTO-GENERATED by scripts/download-dish-photos.mjs.",
  "// Maps a slugified dish name to its photo path under /public/.",
  "// Re-run `node scripts/download-dish-photos.mjs` to refresh.",
  "",
  "export const DISH_PHOTOS: Record<string, string> = {",
  ...sortedKeys.map((k) => `  "${k}": "${manifest[k]}",`),
  "};",
  "",
  "export type DishSlug = keyof typeof DISH_PHOTOS;",
  "",
];
writeFileSync(MANIFEST, lines.join("\n"), "utf8");

console.log(
  `\nDone — ${downloaded} downloaded, ${skipped} skipped, ${failed} failed`
);
console.log(`Manifest: ${MANIFEST}`);
